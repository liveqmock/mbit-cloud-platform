package com.gigfork.api.resource.exceptions;

public class B2BApiException extends Exception {

	private static final long serialVersionUID = -6197574713825855836L;

	public B2BApiException() {
		super();
	}

	public B2BApiException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public B2BApiException(String arg0) {
		super(arg0);
	}

	public B2BApiException(Throwable arg0) {
		super(arg0);
	}

}
